/**
 * Leaves Text Effect - updated for PrimeSA_Bot
 */

const mumaker = require('mumaker');
const config = require('../../config');

module.exports = {
  name: 'leaves',
  aliases: [],
  category: 'textmaker',
  description: 'Create leaves text effect',
  usage: '.leaves <text>',

  async execute(sock, msg, args, extra = {}) {
    const chatId = extra.from || msg.key.remoteJid;
    const reply = extra.reply || (t => sock.sendMessage(chatId, { text: t }, { quoted: msg }));
    const text = args.join(' ').trim();
    if (!text) return reply('❌ Usage: .leaves <text>');

    try {
      await sock.sendPresenceUpdate('composing', chatId).catch(() => {});
      const result = await mumaker.ephoto('https://en.ephoto360.com/green-brush-text-effect-typography-maker-online-153.html', text);
      if (!result || !result.image) throw new Error('No image received');

      await sock.sendMessage(chatId, {
        image: { url: result.image },
        caption: `✨ Generated by ${config.botName}`
      }, { quoted: msg });
    } catch (err) {
      console.error('[leaves] error', err);
      await reply('❌ Failed to generate image.');
    } finally {
      try { await sock.sendPresenceUpdate('paused', chatId); } catch (e) {}
    }
  }
};

